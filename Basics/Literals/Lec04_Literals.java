package Basics.Literals;
public class Lec04_Literals {
    public static void main(String[] args) {
        //literals are the values assigned to a variable
        byte age = 34; //34 is a literal
        int age2 = 56;
        short age3 = 87;
        long ageDino = 5666666666666l;  // must use L or l for long becuz by default its int type
        char ch = 'A';
        float f1= 5.6f;     // must use F or f for float bcuz by default its double
        double d1 = 4.667;  // can use D or d
        boolean a = true;
        String str = "Tasneem";
        System.out.println(age);
        System.out.println(age2);
        System.out.println(age3);
        System.out.println(ageDino);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(a);
        System.out.println(str);
    }
}
